
import datetime

x=datetime.datetime.now() # Get the current date and time
print(x)

print("Date:",x.day)
print("Month:",x.month)
print("Year:",x.year)

print("Hour:",x.hour)
print("Min.:",x.minute)
print("Second:",x.second)
print("Microsecond:",x.microsecond)
print("Weekday:",x.weekday())  # Monday is 0 and Sunday is 6