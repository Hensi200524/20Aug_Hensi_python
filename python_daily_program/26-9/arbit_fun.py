def getdata(*data): # Arbitrary Arguments
    print("ID:",data[0])  # ID of the person
    print("Name:",data[1])  # Name of the person
    print("City:",data[2])  # City of the person


getdata(101,'Hensi','Morbi') # calling function with 3 arguments
getdata(101,'Hensi','Morbi','Python') # calling function with 4 arguments