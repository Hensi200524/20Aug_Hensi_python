"""Accessing tuple elements using positive and negative indexing."""

tuple = ("Heenny","Divya","Krishna","Hensi") # Creating a tuple
print("Positive indexing:")
print(tuple[0]) # Accessing first element
print(tuple[1]) # Accessing second element
print("Negative indexing:")
print(tuple[-1]) # Accessing last element
print(tuple[-2]) # Accessing second last element