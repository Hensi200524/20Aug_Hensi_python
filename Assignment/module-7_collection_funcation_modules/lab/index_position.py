"""Write a Python program to access elements at different index positions."""

list = [22, "Hensi",5.6, True] # Creating a mixed data type list
print(list[0]) # Accessing element at index position 0
print(list[1]) # Accessing element at index position 1
print(list[-1]) # Accessing element at last index position
print(list[-2]) # Accessing element at second last index position